/*
컴퓨터 장치를 쉽게 사용하기위해 운영체제가 관리하고 있음.
운영체제를 이용해서 장치들을 쉽게 사용
컴퓨터 장치들 중 데이터를 저장하기 위해 '메모리' 장치를 사용

메모리: 공공장소의 사물함
	- 크기가 일정한 단위로 구성되어있고 각각 번호(주소)가 붙어있음
	- 메모리 공간을 잘 사용할 수 있도록 운영체제가 나눠서 관리
	- 주소를 사용해서 원하는 메모리를 갖다씀
	- 1byte : 0 ~ 255
	- 1bit : 0 ~ 1 (2^1)
	- 1바이트 단위로 크기가 나눠짐
	- 1byte == 1bit *8개 (2^8)
	- 운영체제는 byte단위로 컨트롤하고 있음
	- 비트연산자를 통해서 bit단위로 컨트롤할 수 있음
*/